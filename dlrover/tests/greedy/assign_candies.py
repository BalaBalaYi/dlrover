"""
n个孩子站成一排。给你一个整数数组 ratings表示每个孩子的评分。
你需要按照以下要求，给这些孩子分发糖果：
1.每个孩子至少分配到 1 个糖果。
2.相邻的孩子中，评分高的孩子必须获得更多的糖果。
请你给每个孩子分发糖果，计算并返回需要准备的 最少糖果数目。

输入: ratings = [1,0,2]
输出: 5
解释: 你可以分别给第一个、第二个、第三个孩子分发 2、1、2 个糖果。

输入: ratings = [1,2,2]
输出: 4
解释: 你可以分别给第一个、第二个、第三个孩子分发 1、2、1 个糖果。
     第三个孩子只得到 1 个糖果，这已满足上述两个条件。
"""

"""
核心思想：
从左到右遍历：如果右边的评分比左边高，则右边的糖果数 = 左边的糖果数 + 1
从右到左遍历：如果左边的评分比右边高，则左边的糖果数 = max(当前糖果数, 右边的糖果数 + 1)
"""


def solve(ratings):
    """
    两次遍历的贪心算法
    """
    n = len(ratings)
    if n == 0:
        return 0

    # 初始化糖果数组，每个孩子至少 1 个糖果
    candies = [1] * n

    # 从左到右遍历：处理递增序列
    for i in range(1, n):
        if ratings[i] > ratings[i - 1]:
            candies[i] = candies[i - 1] + 1

    # 从右到左遍历：处理递减序列
    for i in range(n - 2, -1, -1):
        if ratings[i] > ratings[i + 1]:
            candies[i] = max(candies[i], candies[i + 1] + 1)

    return sum(candies)
