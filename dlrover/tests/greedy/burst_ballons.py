"""有一些球形气球贴在一堵用 XY 平面表示的墙面上。墙面上的气球记录在整数数组 points中，
其中 points[i] = [x_start, x_end]表示水平直径在 x_start和 x_end之间的气球。你不知道气球的确切 y 坐标。
一支弓箭可以沿着 x 轴从不同点 完全垂直 地射出。在坐标 x 处射出一支箭，若有一个气球的直径的开始
和结束坐标为 x_start，x_end，且满足 x_start ≤ x ≤ x_end，则该气球会被 引爆。可以射出的弓箭的数量 没有限制。弓箭一旦被射出之后，可以无限地前进。
给你一个数组 points，返回引爆所有气球所必须射出的 最小 弓箭数。

输入: points = [[10,16],[2,8],[1,6],[7,12]]
输出: 2
解释: 气球可以用2支箭来爆破:
- 在x = 6处射出箭，爆破气球[2,8]和[1,6]
- 在x = 11处射出箭，爆破气球[10,16]和[7,12]

输入: points = [[1,2],[3,4],[5,6],[7,8]]
输出: 4
解释: 每个气球需要一支箭，总共需要4支箭
"""

"""
核心思想：
1.按照气球的结束坐标升序排序
2.第一支箭射在第一个气球的结束位置
3.遍历气球，如果当前气球的开始位置 ≤ 当前箭的位置，则可以被同一支箭引爆
4.否则，需要新的一支箭，射在当前气球的结束位置

"""

def solve(points):
    if not points:
        return 0

    # 按结束坐标升序排序
    points.sort(key=lambda x: x[1])

    arrows = 1
    arrow_pos = points[0][1]  # 第一支箭射在第一个气球的结束位置

    for i in range(1, len(points)):
        # 如果当前气球的开始位置 <= 箭的位置，可以被同一支箭引爆
        if points[i][0] <= arrow_pos:
            continue
        else:
            # 需要新的一支箭
            arrows += 1
            arrow_pos = points[i][1]

    return arrows

