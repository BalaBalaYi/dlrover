"""
假设你是一位很棒的家长，想要给你的孩子们一些小饼干。但是，每个孩子最多只能给一块饼干。
对每个孩子 i，都有一个胃口值 g[i]，这是能让孩子满足的饼干的最小尺寸；并且每块饼干 j，
都有一个尺寸 s[j]。如果 s[j] >= g[i]，我们可以将这个饼干 j分配给孩子 i，这个孩子会得到满足。
你的目标是尽可能满足越多数量的孩子，并输出这个最大数值。

输入: g = [1,2,3], s = [1,1]
输出: 1
解释:
你有三个孩子和两块小饼干，3个孩子的胃口值分别是：1,2,3。
虽然你有两块小饼干，由于它们的尺寸都是1，只能让胃口值是1的孩子满足。
所以你应该输出1。
"""

"""
核心思想：
将孩子的胃口值和饼干尺寸都按升序排序
使用双指针分别指向孩子和饼干
尽可能用最小的饼干满足胃口最小的孩子
"""


def solve(g, s):
    """
    贪心算法：排序 + 双指针
    """
    # 排序
    g.sort()
    s.sort()

    # 双指针
    i = 0  # 孩子指针
    j = 0  # 饼干指针

    while i < len(g) and j < len(s):
        if s[j] >= g[i]:
            # 当前饼干可以满足当前孩子
            i += 1
        # 无论是否满足，都要尝试下一块饼干
        j += 1

    return i
