"""给定一个含有0 到n 整数的数组，每个整数只出现一次，求这个数组最多可以分割成多少个
子数组，使得对每个子数组进行增序排序后，原数组也是增序的。

输入: arr = [4,3,2,1,0]
输出: 1
解释: 将数组分成2块或者更多块，都无法得到所需的结果。
例如，分成 [4, 3], [2, 1, 0] 的结果是 [3, 4, 0, 1, 2]，这不是有序的。

输入: arr = [1,0,2,3,4]
输出: 4
解释: 我们可以把它分成两块，例如 [1, 0], [2, 3, 4]。
然而，分成 [1, 0], [2], [3], [4] 可以得到最多的块数。

核心：贪心 + 最大值边界

由于数组是 [0, 1, ..., n-1]的一个排列，当我们将数组正确排序后，每个数字应该出现在它的索引位置上。

关键观察：

对于一个有效的分割块，块内的最大值必须等于这个块的结束索引。因为：
块内的数字排序后，最大值应该出现在块的最后一个位置
而这个块的最大值在原排序中应该在索引为最大值的位置
所以块的结束索引必须等于块内的最大值
算法步骤：
遍历数组，记录到当前位置为止的最大值
当最大值等于当前位置的索引时，说明找到了一个有效的分割块
计数器加1
继续遍历直到数组结束
时间复杂度：O(n)
空间复杂度：O(1)
"""

def solve(arr):
    max_val = 0
    count = 0

    for i, num in enumerate(arr):
        max_val = max(max_val, num)
        if max_val == i:
            count += 1

    return count


if __name__ == '__main__':
    print(solve([4, 3, 2, 1, 0]))  # 1
    print(solve([1, 0, 2, 3, 4]))  # 4
    print(solve([0]))  # 1
