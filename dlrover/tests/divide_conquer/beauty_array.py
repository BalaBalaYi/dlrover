"""对于某些固定的 N，如果数组 A是整数 1, 2, ..., N组成的排列，使得：
对于每个 i < j，都不存在 k满足 i < k < j使得 A[k] * 2 = A[i] + A[j]。
换句话说，数组中任意三个位置 i < k < j，都不能满足 A[k] * 2 = A[i] + A[j]。
我们称这样的数组为漂亮数组。
给定 N，返回任意一个漂亮数组。保证存在答案。

输入: N = 4
输出: [2,1,4,3]
解释: 验证条件：不存在 k 使得 A[k]*2 = A[i]+A[j]

输入: N = 5
输出: [3,1,2,5,4]

核心：分治 + 奇偶构造

这是一个巧妙的分治问题。关键在于观察到：

漂亮数组的两个性质：
如果 A是漂亮数组，那么：
对每个元素乘以一个常数后仍是漂亮数组
对每个元素加上一个常数后仍是漂亮数组
如果 A是漂亮数组，那么：
所有奇数组成的子数组是漂亮数组
所有偶数组成的子数组是漂亮数组
关键构造方法：
从 [1]开始
将当前数组的每个元素乘以 2 减 1，得到全奇数数组
将当前数组的每个元素乘以 2，得到全偶数数组
将两部分拼接起来
为什么这样构造有效？

对于任意 i < k < j：
如果 i, j, k都在奇数部分：奇数+奇数=偶数，但中间是奇数，不满足 2×奇数=偶数
如果都在偶数部分：同理
如果 i在奇数部分，j在偶数部分：奇数+偶数=奇数，但中间数是偶数，不满足

"""

def solve(N):
    memo = {1: [1]}

    def f(n):
        if n not in memo:
            # 构造奇数部分
            odds = f((n + 1) // 2)  # ceil(n/2)
            # 构造偶数部分
            evens = f(n // 2)  # floor(n/2)

            # 拼接：所有奇数在前，所有偶数在后
            # 奇数 = 2×x - 1
            # 偶数 = 2×x
            memo[n] = [2 * x - 1 for x in odds] + [2 * x for x in evens]
        return memo[n]

    return f(N)


if __name__ == '__main__':
    print(solve(4))  # [2,1,4,3] 或其他合法答案
    print(solve(5))  # [1,5,3,2,4] 或其他合法答案
