"""
给定一个整数，判断它是否是4 的次方。

核心判断条件:

一个整数 n是 4 的次方，必须同时满足以下两个条件：
它是 2 的次方：即 n的二进制表示中有且只有 1 个 1。
这个 1出现在奇数位：即 n的二进制表示中，唯一的那个 1必须出现在从右往左数的第 1、3、5、7... 位（即奇数位）。
具体实现方法

我们可以通过以下位运算技巧来验证：
检查是否为 2 的次方：
使用 n & (n - 1) == 0。这个操作能去掉二进制中最低位的 1，如果去掉后结果为 0，说明原来只有一个 1。
检查 1 是否在奇数位：
使用 n & 0x55555555 != 0。0x55555555的二进制是 01010101...（全是奇数位为 1）。如果 n与它进行与运算结果不为 0，说明 n的 1 确实落在了奇数位上。

"""

def is_power_of_4(n):
    return n > 0 and (n & (n - 1) == 0) and (n & 0x55555555) != 0
