"""给定一个整数数组 prices和一个整数 k，其中 prices[i]是某支给定的股票在第 i天的价格。
设计一个算法来计算你所能获取的最大利润。你最多可以完成 k 笔交易。
注意：你不能同时参与多笔交易（你必须在再次购买前出售掉之前的股票）。

输入：k = 2, prices = [2,4,1]
输出：2
解释：在第 1 天 (股票价格 = 2) 买入，在第 2 天 (股票价格 = 4) 卖出，这笔交易所能获得利润 = 4-2 = 2。

输入：k = 2, prices = [3,2,6,5,0,3]
输出：7
解释：
第 2 天 (股票价格 = 2) 买入，第 3 天 (股票价格 = 6) 卖出，这笔交易所能获得利润 = 6-2 = 4。
随后，第 5 天 (股票价格 = 0) 买入，第 6 天 (股票价格 = 3) 卖出，这笔交易所能获得利润 = 3-0 = 3。
总利润为 4 + 3 = 7。
"""


"""
状态定义：
对于第 i 天，我们定义两种状态：
1. dp[t][i][0]: 第 i 天结束时，最多完成了 t 笔交易，且不持有股票的最大利润
2. dp[t][i][1]: 第 i 天结束时，最多完成了 t 笔交易，且持有股票的最大利润

状态转移：
1. 不持有股票
// 1. 第 i 天不操作，保持不持有状态 dp[t][i-1][0]
// 2. 第 i 天卖出股票（完成一笔交易）dp[t][i-1][1] + prices[i]
dp[t][i][0] = max(dp[t][i-1][0], dp[t][i-1][1] + prices[i])

2. 持有股票
// 1. 第 i 天不操作，保持持有状态 dp[t][i-1][1]
// 2. 第 i 天买入股票（开始一笔新交易）dp[t-1][i-1][0] - prices[i]
dp[t][i][1] = max(dp[t][i-1][1], dp[t-1][i-1][0] - prices[i])

初始化：
第 0 天（还没开始）：
dp[t][0][0] = 0：不持有股票，利润为 0
dp[t][0][1] = -∞：不可能持有股票

交易次数为 0：
dp[0][i][0] = 0：没有交易，不持有股票
dp[0][i][1] = -∞：没有交易，不可能持有股票
"""


def solve(prices, k):
    if not prices or k <= 0:
        return 0

    n = len(prices)
    # 如果k很大，相当于不限制交易次数
    if k >= n // 2:
        profit = 0
        for i in range(1, n):
            if prices[i] > prices[i - 1]:
                profit += prices[i] - prices[i - 1]
        return profit

    # 初始化
    dp = [[[0] * 2 for _ in range(n)] for _ in range(k + 1)]
    # 初始化交易次数为 0 的情况
    for i in range(n):
        dp[0][i][0] = 0
        dp[0][i][1] = float('-inf')

    # 初始化第 0 天的情况
    for t in range(1, k + 1):
        dp[t][0][0] = 0
        dp[t][0][1] = float('-inf')

    for t in range(1, k + 1):
        for i in range(1, n):
            # 第 i 天不持有股票
            dp[t][i][0] = max(dp[t][i - 1][0], dp[t][i - 1][1] + prices[i])
            # 第 i 天持有股票
            dp[t][i][1] = max(dp[t][i - 1][1], dp[t - 1][i - 1][0] - prices[i])

    return dp[k][-1][0]


if __name__ == '__main__':
    print(solve([2,4,1], 2))  # 2
    print(solve([3,2,6,5,0,3], 2))  # 7
