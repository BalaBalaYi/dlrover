"""如果连续数字之间的差严格地在正数和负数之间交替，则数字序列称为 摆动序列。第一个差（如果存在的话）可能是正数或负数。
仅有一个元素或者含两个不等元素的序列也视作摆动序列。
例如，[1, 7, 4, 9, 2, 5]是一个摆动序列，因为差值 (6, -3, 5, -7, 3)交替为正负。
相反，[1, 4, 7, 2, 5]不是摆动序列，因为它的前两个差值都是正数。
给你一个整数数组 nums，返回 nums中作为摆动序列的最长子序列的长度。

输入：nums = [1,7,4,9,2,5]
输出：6
解释：整个序列均为摆动序列，各元素之间的差值为 (6, -3, 5, -7, 3) 。

输入：nums = [1,17,5,10,13,15,10,5,16,8]
输出：7
解释：这个序列包含几个长度为 7 的摆动序列。
其中一个可表示为：[1, 17, 10, 13, 10, 16, 8]，差值序列为 (16, -7, 3, -3, 6, -8)。
"""


"""
状态定义：
up[i]：以第 i 个元素结尾，且最后呈上升趋势（nums[i] > nums[i-1]）的最长摆动子序列长度
down[i]：以第 i 个元素结尾，且最后呈下降趋势（nums[i] < nums[i-1]）的最长摆动子序列长度

状态转移：
对于每个 i 从 1 到 n-1：
1. 如果 nums[i] > nums[i-1]:
   up[i] = down[i-1] + 1   # 从下降转为上升
   down[i] = down[i-1]     # 保持下降状态
2. 如果 nums[i] < nums[i-1]:
   down[i] = up[i-1] + 1   # 从上升转为下降
   up[i] = up[i-1]         # 保持上升状态
3. 如果 nums[i] == nums[i-1]:
   up[i] = up[i-1]
   down[i] = down[i-1]

初始化：
up[0] = 1：单个元素视为上升或下降都可以，长度为 1
down[0] = 1
"""


def solve(nums):
    n = len(nums)
    if n < 2:
        return n

    up = [1] * n  # 以 nums[i] 结尾，最后上升的最长摆动序列长度
    down = [1] * n  # 以 nums[i] 结尾，最后下降的最长摆动序列长度

    for i in range(1, n):
        if nums[i] > nums[i - 1]:
            # 上升：从下降状态转为上升
            up[i] = down[i - 1] + 1
            down[i] = down[i - 1]
        elif nums[i] < nums[i - 1]:
            # 下降：从上升状态转为下降
            down[i] = up[i - 1] + 1
            up[i] = up[i - 1]
        else:
            # 相等：保持不变
            up[i] = up[i - 1]
            down[i] = down[i - 1]

    return max(up[-1], down[-1])
