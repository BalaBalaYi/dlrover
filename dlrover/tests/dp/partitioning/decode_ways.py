"""已知字母A-Z 可以表示成数字1-26。给定一个数字串，求有多少种不同的字符串等价于这个
数字串。

输入：s = "12"
输出：2
解释：它可以解码为 "AB"（1 2）或者 "L"（12）。

输入：s = "226"
输出：3
解释：它可以解码为 "BZ" (2 26), "VF" (22 6), 或者 "BBF" (2 2 6)。
"""

"""
核心思路

这是一个典型的 动态规划 问题。我们可以将字符串 s看作一个序列，每个位置 i有两种解码可能：
1.单独解码：将 s[i]单独视为一个数字（1-9），对应一个字母。
2.组合解码：将 s[i-1]和 s[i]组合成一个两位数（10-26），对应一个字母。


状态定义

定义 dp[i]表示字符串 s的前 i个字符（即 s[0...i-1]）的解码方法总数。

状态转移方程

对于第 i个字符（即 s[i-1]），我们考虑两种情况：
1.情况一：单独解码
    条件：s[i-1] != '0'（因为 '0'不能单独映射为字母）。
    转移：如果 s[i-1]可以单独解码，那么前 i个字符的解码数至少等于前 i-1个字符的解码数。
    公式：dp[i] += dp[i-1]
2.情况二：组合解码
    条件：i >= 2（保证有前一个字符），且 s[i-2] != '0'（防止出现 "0X"这种无效组合），且 s[i-2]和 s[i-1]组成的两位数在 10到 26之间。
    转移：如果 s[i-2]和 s[i-1]可以组合解码，那么前 i个字符的解码数还要加上前 i-2个字符的解码数。
    公式：dp[i] += dp[i-2]
"""

def solve(s):
    n = len(s)
    dp = [0] * (n + 1)

    # 空字符串有一种解码方式
    dp[0] = 1

    for i in range(1, n + 1):
        # 单独解码：i-1（第i个）组成1个字母
        if s[i - 1] != '0':
            dp[i] += dp[i - 1]
        # 组合解码: i-1（第i个）和i-2（第i-1个）组成1个字母
        if i >= 2 and s[i - 2] != '0' and 10 <= int(s[i - 2:i]) <= 26:
            dp[i] += dp[i - 2]

    return dp[n]


if __name__ == '__main__':
    print(solve("226"))

