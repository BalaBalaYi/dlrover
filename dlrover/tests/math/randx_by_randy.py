"""已有方法 rand7可生成 1 到 7 范围内的均匀随机整数，试写一个方法 rand10生成 1 到 10 范围内的均匀随机整数。

核心：拒绝采样

关键思想是通过 rand7()生成一个均匀分布的更大范围的随机数，然后通过拒绝采样（rejection sampling）得到 1-10 的均匀分布。

基本思路：
用 rand7()生成两位 7 进制数：(rand7()-1) * 7 + (rand7()-1)
这样得到 0-48 的均匀分布（共 49 个数）
拒绝 40-48 这 9 个数，只取 0-39
对 0-39 取模 10 再加 1，得到 1-10
为什么是 7 进制？
7 进制的个位：rand7()-1→ 0-6
7 进制的十位：(rand7()-1) * 7→ 0,7,14,21,28,35,42
组合：0-48 的 49 个等概率数
"""


def rand7():
    """已有的随机数生成器（题目提供）"""
    import random
    return random.randint(1, 7)


def rand10():
    """
    用 rand7() 实现 rand10()
    期望调用 rand7() 次数: 2.45 次
    时间复杂度: 期望 O(1)，最坏 O(∞)
    空间复杂度: O(1)
    """
    while True:
        # 生成 0-48 的均匀随机数
        # (rand7()-1) * 7 生成 0,7,14,21,28,35,42
        # (rand7()-1) 生成 0-6
        num = (rand7() - 1) * 7 + (rand7() - 1)

        # 拒绝 40-48，只取 0-39
        if num < 40:
            return num % 10 + 1


